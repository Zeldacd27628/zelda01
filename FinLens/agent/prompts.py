MANIPULATION_DETECTION_PROMPT = """## 核心任务：会计操纵信号深度检测

**这是FinLens最核心的功能模块。**

你拥有100万Token的完整年报上下文，请像专业审计师一样深度阅读年报，
发现任何可能表明会计操纵或财务造假的信号。

### 公司信息
- 公司名称：{company_name}
- 所属行业：{industry}

### 已计算的财务指标
```json
{financial_metrics}
```

### 完整年报内容（已脱敏）
{pdf_text}

---

## 检测维度

### 1. 应收账款异常
**关注点**：
- 应收账款增速是否远高于营业收入增速（差异超过30%需警惕）
- 应收账款占营收比例是否异常偏高
- 是否存在大额应收账款长期挂账
- 应收账款的账龄结构和坏账准备是否合理
- 关联应收账款情况

**分析方法**：
- 计算：应收增速 - 营收增速 = ?
- 查看应收账款附注中的客户集中度
- 检查是否有异常的大额应收账款

### 2. 存货异常
**关注点**：
- 存货增速是否远高于营收或成本增速
- 存货周转率是否持续下降
- 存货结构是否异常（如在产品占比过高）
- 存货跌价准备是否充足
- 是否存在大量长期积压存货

**分析方法**：
- 横向对比：存货增速 vs 营收增速
- 纵向对比：存货周转天数变化趋势
- 检查存货附注中的库龄分布

### 3. 现金流与净利润背离
**关注点**：
- 经营现金流长期低于净利润（可能虚增利润）
- 应收类项目增长过快
- 存货积压导致现金流恶化
- 经营性负债的异常变化

**分析方法**：
- 计算：经营现金流/净利润 比率
- 分析现金流表的各个组成项
- 关注"经营活动现金流/净利润"连续多年低于0.8的预警

### 4. 会计政策变更
**关注点**：
- 是否频繁变更会计政策和会计估计
- 变更时机是否"巧合"（如变更在亏损年份）
- 变更理由是否合理充分
- 变更对利润的影响方向和金额

**分析方法**：
- 搜索"会计政策变更"、"会计估计变更"相关披露
- 量化各期变更对利润的影响
- 评估变更理由的商业合理性

### 5. 关联交易异常
**关注点**：
- 关联交易金额和占比是否过高
- 关联交易定价是否公允
- 是否存在隐蔽的关联关系
- 关联方往来余额是否异常

**分析方法**：
- 汇总各类关联交易金额
- 计算关联交易占营收/采购的比例
- 检查关联定价的披露质量

### 6. 其他应收款异常
**关注点**：
- 其他应收款金额是否异常大
- 是否有大额关联方往来
- 是否存在资金占用情况
- 账龄结构是否合理

**分析方法**：
- 计算其他应收款占总资产比例
- 查看明细构成
- 关注是否有大额押金保证金

### 7. 商誉减值问题
**关注点**：
- 商誉规模是否过大
- 减值测试时是否充分考虑减值迹象
- 是否存在"业绩精准达标"后大额减值
- 商誉占净资产比例是否过高

**分析方法**：
- 计算商誉/净资产 比率
- 检查历年商誉减值情况
- 分析并购标的的业绩完成情况

### 8. 研发费用资本化
**关注点**：
- 资本化率是否异常偏高
- 资本化金额是否与研发项目匹配
- 资本化政策是否合理且一贯
- 是否通过资本化调节利润

**分析方法**：
- 计算：研发资本化金额/研发投入总额
- 与同业对比资本化率
- 检查资本化时点和依据

### 9. 预付款项异常
**关注点**：
- 预付款项增长是否异常
- 是否存在大额关联方预付
- 预付款账龄是否合理
- 是否存在虚构预付款转移资金

**分析方法**：
- 计算预付款增速
- 检查预付款对手方
- 分析账龄结构变化

### 10. 审计意见
**关注点**：
- 是否为非标准审计意见
- 强调事项段的性质和影响
- 关键审计事项的领域
- 与前任审计师的意见差异

---

## 输出要求

请仔细阅读年报全文，结合以上检测维度，系统性地识别所有可疑信号。

**输出格式**（必须是有效的JSON）：
```json
{{
  "signals": [
    {{
      "type": "信号类型（如：receivable_anomaly）",
      "level": "高风险/中风险/低风险",
      "title": "信号标题",
      "description": "详细描述",
      "evidence": [
        "具体证据1",
        "具体证据2"
      ],
      "impact": "对财务数据的影响",
      "recommendation": "建议的核实方向"
    }}
  ],
  "overall_assessment": "总体评估",
  "confidence": 0.85,
  "context_used": "本次分析使用的上下文长度（Token数）"
}}
```

**重要**：
1. 每个信号都必须有具体的证据支撑
2. 要充分利用年报全文的上下文，关联分析
3. 区分"可疑"和"确认"，不要过度推断
4. 对于不明确的情况，给出"建议进一步核实"而非直接下结论"""

